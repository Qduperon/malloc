#include "libft.h"

int		ft_isspace(int i)
{
	unsigned char c;

	c = (unsigned char)i;
	if (c == '\t' || c == '\n' || c == ' ' || \
			c == '\v' || c == '\r' || c == '\f')
		return (1);
	return (0);
}
